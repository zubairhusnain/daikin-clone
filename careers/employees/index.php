<h1>Global Activities of <br>
Daikin Employees</h1>
<p class="blueText">In advancing global business expansion, Daikin Employees are also actively involved in contributing to the environments, lifestyles, and economic development of the countries and regions Daikin serves. Discover how they feel about being a member of a multinational company in a fast-growing industry.</p>
<p class="blackText">Global Opportunities for <br>
You to Discover</p>
<p class="blackText_sub">From our head office located in Osaka, Japan, the Daikin Group has rapidly expanded business operations around the world to China, Asia, Oceania, Europe, and the Americas. Overseas sales now account for over 83% of our total sales, and four-fifths of our employees work outside of Japan. In addition to offering products that uniquely meet customer needs, Daikin provides opportunities to all employees working in the Daikin Group to demonstrate their individuality and desire while respecting the diverse lifestyles and values of the countries and regions where they live. Contribution to local community development is another important aspect of our global approach.</p>
