<h1>Desired<br>
Human Resources</h1>
<p class="blackText">Characteristics Essential for Success <br>
in the Global Market</p>
<p class="blackText_02">Leveraging individuality in self and others within diversity</p>
<p class="justyfy">Daikin recognizes that each person has individual traits and opinions originating
from differences in backgrounds and beliefs, and it is that diversity in people that generates
new revolutionary concepts. For this reason, Daikin readily accepts people of diverse values,
independent streaks, and distinctive personalities. Together with people capable of
demonstrating individuality in their work, we seek people that respect and harness the unique
qualities of those surrounding them. </p>
</div>
<div class="threebox">
