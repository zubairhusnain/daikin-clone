<div class="textArea">
<p class="blackText_02 justyfy">To these types of people desired by Daikin, we ask that they
take part in the Daikin Group as members sharing the following three aspirations as a
foundation. </p>
<ul>
    <li>People who closely identify with the Daikin Group’s ambitions and have a strong desire
    to participate in the company.<br>
    <span>- Consequently, people who feel a sense of loyalty and take pride in the company
    and their work -</span>
