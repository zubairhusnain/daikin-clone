            <div class="g-section">
                            <p class="g-p"><strong>OSAKA, Japan, May 27 2026</strong> - Daikin Industries, Ltd. has joined the Global Alliance for Buildings and Construction (GlobalABC), an international initiative aimed at promoting decarbonization in the building and construction sector.</p>

<div class="g-media-auto g-talign-c">
  <div class="g-media_left"><img src="assets/www.daikin.com/press/2026/-/media/Project/Daikin/daikin_com/press/2026/20260527/images/20260527_1.png" alt="" class="g-width-80 g-width-touch-100">
  </div>
</div>

<div class="g-section-lv2">
          <p class="g-p">GlobalABC is an international platform led by the United Nations Environment Programme (UNEP), bringing together more than 400 stakeholders, including governments, international organizations, and companies, to improve energy efficiency and reduce greenhouse gas emissions in the building and construction sector. Through international collaboration, the initiative advances global decarbonization efforts by providing guidance for the development of national roadmaps and facilitating discussions through working groups.</p>
          <p class="g-p">Daikin has been strengthening its collaboration with GlobalABC through participation in various events and ongoing dialogue. At COP30, held in Belém, Brazil, in November 2025, Daikin participated in a session hosted by GlobalABC and shared its initiatives and perspectives on accelerating the adoption of highly energy-efficient air conditioners and heat pump heating technologies.</p>
          <p class="g-p">As climate change becomes an increasingly critical global issue, the importance of decarbonization in the building and construction sector continues to grow. Buildings such as homes, offices, and commercial facilities account for approximately 40% of global CO₂ emissions. In particular, energy consumption for heating and cooling represents a significant portion of these emissions. Recognizing the important role Daikin can play globally in addressing these challenges, we decided to join GlobalABC.</p>
          <p class="g-p">In recent years, in addition to reducing emissions associated with energy consumption during building operation, there is growing emphasis on “whole life carbon” emissions across the entire building lifecycle, including the manufacturing of materials and equipment, construction, and demolition. Through its participation in GlobalABC, Daikin will further strengthen collaboration with stakeholders across the building and construction  sector and contribute to reducing greenhouse gas emissions throughout the lifecycle of buildings.</p>
          <p class="g-p">“As a global leader in the air conditioning industry, Daikin will continue to steadily advance the adoption and implementation of solutions that contribute to decarbonization in the building and construction sector,” said Katsuyuki Sawai, Senior Executive Officer at Daikin Industries, responsible for CSR, Global Environment, and External Relations. “At the same time, we will work to help create an environment in which the values of these solutions are properly recognized and supported. Through collaboration with the international community, we will aim to contribute to a more sustainable society.”</p>
          
<div class="g-section-lv2">
<h3 class="g-ttl-3">DAIKIN Spoke at  the Sustainable Buildings and Construction Summit (SBCS) 2026</h3>

          <p class="g-p">Daikin spoke at the Sustainable Buildings and Construction Summit (SBCS) 2026, an international conference hosted by GlobalABC from April 20 to 22 in Lausanne, Switzerland, where the company shared its initiatives and perspectives on decarbonization in the building and construction sector.</p>
          <p class="g-p">SBCS serves as a leading global platform for discussions on decarbonization in the building and construction sector. The conference brought together 468 participants from 64 countries, including representatives from international organizations, governments, private companies, and financial institutions, to accelerate concrete actions across policy, technology, and finance.</p>
          <p class="g-p">At a plenary panel discussion during the conference, Kazuma Koyama, Senior Manager, External Relations Dept. at Daikin Industries, Ltd., highlighted the importance of not only developing new technologies but also expanding the adoption of existing high-efficiency technologies such as heat pumps, as well as strengthening lifecycle refrigerant management across society to advance decarbonization in the building and construction sector. He also emphasized that collaboration with policymakers is essential to accelerate the broader implementation of advanced existing technologies and introduced Daikin’s proactive involvement in policy development. Furthermore, he expressed the view that appropriate recognition and support from building standards and financial markets would help create new business opportunities across the entire value chain.</p>
          <p class="g-p">In addition, Patrick Crombez, Deputy General Manager, Environmental Research Center at Daikin Europe N.V., spoke at   a plenary presentation session. He presented case studies demonstrating the effectiveness of replacing fossil-fuel-based heating systems with heat pumps in existing buildings. He also explained that optimizing building performance, construction methods, and equipment technologies in an integrated manner is key to decarbonization through wider adoption of heat pumps. Furthermore, he introduced perspectives on improving installation efficiency through standardization and modularization, addressing labor shortages, and contributing to greater flexibility in power systems.</p>
          
                   


