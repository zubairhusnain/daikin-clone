            <div class="g-section">
                <p class="g-p"><strong><span>Brussels, May&nbsp;</span></strong><strong>21,&nbsp;</strong><strong>2026 </strong>– Daikin Europe N.V. today held the official inauguration of Daikin Manufacturing Poland Sp. z o.o., a new heat pump production facility located in the Łódź region. The opening ceremony brought together representatives of the Polish government, the Ambassador of Japan to Poland, local authorities, suppliers, and other partners, alongside senior leadership from Daikin Industries Ltd., Daikin Europe, and Daikin Manufacturing Poland, underscoring the strong partnerships behind the project.</p>
<div class="g-media-auto g-talign-c g-mt-50">
<div class=""><img src="assets/www.daikin.com/press/2026/-/media/Project/Daikin/daikin_com/press/2026/20260522/images/20260522_1.jpg" alt="" class="g-width-80 g-width-touch-100">
</div>
</div>
<h3 class="g-ttl-3 g-mt-50">Major investment strengthening Daikin’s European manufacturing network</h3>
<p class="g-p">The new facility represents an investment of approximately €300 million and serves as a key production base within Daikin’s European manufacturing footprint. The factory primarily produces residential heat pump heating systems, to meet the growing demand for sustainable heating solutions across Europe.&nbsp;</p>
<p class="g-p">At the inauguration, Masanori Togawa, Representative Director, Chairman of the Board and CEO of Daikin Industries, stated: “This facility marks an important milestone for our global operations and reflects our long-standing philosophy of producing close to the market. By strengthening our manufacturing presence in Europe, we can respond more quickly to local needs and build long-term partnerships across the region.”
</p>
<h3 class="g-ttl-3 g-mt-50">Poland selected as a strategic location for long-term growth&nbsp;</h3>
<p class="g-p">The decision to establish the plant in Poland reflects the country’s highly skilled workforce, strong industrial and academic base, as well as excellent access to major European markets. The city of Łódź, with its long-standing manufacturing and innovation heritage, offers a stable and attractive environment for long-term business development.&nbsp;</p>
<p class="g-p">Yasuto Hiraoka, Managing Director of Daikin Manufacturing Poland, commented: “Poland offers the capabilities and environment we need for long-term growth. Together with our partners and the local community, we aim to contribute to the sustainable development of this region while building a strong and trusted production base.”
</p>
<h3 class="g-ttl-3 g-mt-50">Supporting Europe’s energy transition through local production</h3>
<p class="g-p">From a European perspective, Daikin Europe emphasized that the facility plays a role that goes beyond manufacturing. By enabling local production of heat pumps, the plant supports Europe’s transition away from fossil-fuel-based heating, while contributing to CO₂ emission reductions and greater energy efficiency.&nbsp;</p>
<p class="g-p">In this context, Masatsugu Minaka, Chairman of Daikin Europe N.V., highlighted the broader significance, stating: “This factory carries importance not only for Daikin, but also for Europe. Local manufacturing of heat pumps accelerates the region’s energy transition, strengthens supply chain resilience, and contributes to a more sustainable industrial base.”
</p>
<p class="g-p">The facility also reinforces Daikin’s ‘local for local’ approach by enabling a faster response to customer needs and ensuring a stable supply of energy-efficient solutions. Close cooperation with the company’s global R&amp;D hub in Ghent (Belgium) established in 2025, will further accelerate the transfer of advanced technologies into large-scale production.</p>
<h3 class="g-ttl-3 g-mt-50">Contributing to regional development and long-term partnerships</h3>
<p class="g-p">Beyond its industrial role, the new factory is expected to contribute to regional development through skills development, and long-term engagement with local communities and partners.</p>
<p class="g-p">Michał Jaros, Secretary of State, Ministry of Economic Development and Technology, commented: “The inauguration of this new production facility marks an important step for Poland’s industrial development and Europe’s energy transition. Investments such as this contribute to strengthening our manufacturing base, creating high-quality jobs, and supporting the development of skills and innovation in the region. We welcome Daikin’s long-term commitment to Poland and its contribution to building more sustainable and energy-efficient solutions for Europe. This project also reflects the importance of close cooperation between international partners, local communities, and public institutions.”
</p>
<p class="g-p">The inauguration highlighted the shared commitment of all stakeholders to strengthen cooperation and support sustainable economic growth, both in Poland and across Europe.</p>

