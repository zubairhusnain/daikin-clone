<div class="g-section-lv2">
<h3 class="g-ttl-3">Overview of the Invention</h3>


          <p class="g-p">In recent years, growing concern over environmental issues such as CO<sub>2</sub> emissions and food loss has accelerated demand for lower-impact food transportation methods, shifting from air freight to maritime shipping. Fresh produce and flowers continue to respire after harvest, making it essential to properly manage oxygen and carbon dioxide concentrations during transportation and storage in order to suppress respiration and maintain freshness over longer periods.</p>
          <p class="g-p">Conventional CA technologies have primarily relied on “passive” systems that remove carbon dioxide generated inside containers. However, these systems faced challenges such as slow reduction of CO<sub>2</sub> concentration and difficulty maintaining stable internal conditions due to the intrusion of outside air.</p>
          <p class="g-p">To address these issues, Daikin applied its high-concentration oxygen generation technology developed for medical oxygen concentrators to create an “active” CA system. The system processes outside air through adsorption cylinders and supplies low-oxygen gas containing higher concentrations of nitrogen and moisture. In addition, the system features four control modes that switch low-O<sub>2</sub> gas concentrations according to the type of produce, respiration rate, and transportation conditions, enabling precise control of O<sub>2</sub> and CO<sub>2</sub> concentrations inside the container.</p>
       
