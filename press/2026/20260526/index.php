            <div class="g-section">
                          <p class="g-p"><strong>OSAKA, JAPAN, 26 May 2026</strong> - Daikin Industries, Ltd. has received the “Prize of the Chairman of the Japan Institute of Invention and Innovation (JIII)” at the 2026 National Commendation for Invention, organized by the Japan Institute of Invention and Innovation, for its “Invention of an Atmosphere Control System Enabling Long-Distance Maritime Container Transport of Fresh Produce.” The award ceremony is scheduled to be held on June 15 at The Okura Tokyo. This marks the fourth time Daikin has received a special award or higher recognition at the National Commendation for Invention.</p>
          <p class="g-p">Established in 1919, the National Commendation for Invention is an award program aimed at contributing to the advancement of science and technology and the development of industry in Japan. The program recognizes outstanding inventions, ideas, and designs that have made significant contributions or are expected to contribute substantially in the future due to their excellence.</p>
          <p class="g-p">The awarded invention applies Controlled Atmosphere (CA) technology to enable long-distance maritime transport of fresh produce while maintaining freshness over extended periods. By controlling oxygen and carbon dioxide concentrations inside shipping containers according to the respiration rate of produce, the system helps preserve freshness throughout transportation. The invention was highly evaluated as a technology that contributes to the global transportation of a wider variety of fruits and vegetables while reducing environmental impact compared with air freight.</p>

<div class="g-media-auto g-talign-c">
  <div class="g-media_left"><img src="assets/www.daikin.com/press/2026/-/media/Project/Daikin/daikin_com/press/2026/20260526/images/20260526_1.jpg" alt="" class="g-width-80 g-width-touch-100">
  </div>
</div>

          <p class="g-p">Leveraging refrigeration, freezing, and air control technologies cultivated through its air conditioning business, Daikin has long pursued the creation of new value not only in comfortable indoor environments, but also in fields such as industry, food distribution, and agriculture. Although air itself is invisible, optimizing factors such as temperature, humidity, airflow, and gas concentration holds significant potential for preserving the freshness of produce, reducing food loss, and enabling more sustainable supply chains.</p>

          <p class="g-p">As a company committed to “Perfecting the Air,” Daikin will continue advancing its air control technologies to help reduce environmental impact and address societal challenges, thereby contributing to the realization of a more sustainable society.</p>

