<h3 class="g-ttl-3">Inverter Technology</h3>
<p>Air conditioner compressors are driven by motor, and motor rotation speed depends on power supply frequency. An inverter modulates power supply frequency to control motor rotation speed. Inverters stabilize temperature by adjusting compressor operation according to load to eliminate waste and save energy.</p>
<p>Even adopting an inverter to the fan motors of the indoor and outdoor units provides more precise control and contributes to energy savings.</p>
