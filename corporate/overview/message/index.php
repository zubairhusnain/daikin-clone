<h1 class="g-ttl-1">Message from Management</h1>
<div class="mainVisual"><img src="assets/www.daikin.com/-/media/Project/Daikin/daikin_com/corporate/overview/message/images/CEO_message-png.png" class="g-img" style="">
</div>

<div class="g-section-lv2">
<h2 class="g-ttl-2">Harnessing Our Strengths to Adapt to a Changing World</h2>
<p>Today, the world and economic trends are changing rapidly, the future is uncertain, and the successful experiences of the past are no longer applicable. In fiscal 2023, the Daikin Group further strengthened its five-year Fusion 25 strategic management plan that began in 2021 and formulated a three-year plan for the latter half. Recognizing that change is an opportunity, we will take the lead and aim for further growth.</p>
</div>

<div class="g-section-lv2">
<h2 class="g-ttl-2">Working to Resolve Social Issues as Part of Our Social Responsibilities as an Air Conditioner Manufacturer</h2>
<p>The Group&rsquo;s core business of air conditioning is an important form of infrastructure supporting our daily lives. Approximately 1.1 billion people around the world are at risk of losing their lives due to extreme heat and demand for air conditioners is expected to increase in the future for reasons such as health and economic development.</p>
<p>Air conditioning, on the other hand, consumes enormous amounts of energy. The amount of electricity consumed by the use of air conditioners accounts for about 10% of the world&rsquo;s total electricity consumption. For this reason, attention is now focusing on how to control this from increasing in the future.</p>
<p>Given these circumstances, we believe that addressing society's needs for air solutions and contributing to the decarbonization of society is the Group's most important social mission. In 2018, we formulated Environmental Vision 2050, under which we aim to achieve net-zero greenhouse gas emissions, and set &ldquo;Challenge to achieve carbon neutrality&rdquo; as one of the key strategic themes of Fusion 25.</p>
<p>In the three-year plan for the latter half of Fusion 25, we have strengthened our initiatives to decarbonize and achieve a circular economy, which is seeing increasing calls from society due to the faster pace of changes in the external environment. We will further promote the spread of environmentally conscious products using inverter technology and refrigerants with lower global warming potential. In addition, we have set a new target to reach net-zero greenhouse gas emissions by 2030 at all plants except chemical plants, and in fiscal 2023, the Sakai Plant-Rinkai Factory was the first to achieve this. We will continue to improve the energy efficiency of production facilities and introduce renewable energy at our plants around the world to achieve our targets. We will also add &ldquo;circular economy initiatives&rdquo; as a new area to strengthen and give top priority to building a refrigerant recovery and reclamation system vital for air conditioners.</p></div>

<div class="g-section-lv2">
<h2 class="g-ttl-2">Workforce Diversity as a Driver Behind Our Competitiveness</h2>
<p>Currently, human capital management is attracting attention because it aims to elevate the capabilities and motivation of the talent underpinning companies and to increase corporate value. This is exactly what the Group has been practicing as people-centered management.</p>
<p>The Group&rsquo;s greatest strength is diversity management, which believes in the infinite potential of people and makes the most of each person&rsquo;s individuality. The source of our competitiveness has been our diverse workforce that embraces and utilizes each other&rsquo;s values, our efforts to improve our organizational strengths, and the pursuit of ambitious goals. We will continue to be a company that develops human resources from a medium- to long-term perspective and provides employees around the world with the joy of working with passion and opportunities to take on challenges. Going forward, we will harness the abilities of each and every one of our people to lead to the sustainable growth and development of society and the Group.</p>
</div>

<div class="g-section-lv2">
<h2 class="g-ttl-2">Aspiring for Greater Growth and Development on Our 100th Anniversary</h2>
<p>Daikin celebrated its 100th anniversary in 2024. We have accelerated our overseas expansion by leveraging our air conditioning and fluorochemical technologies to grow into a global company with operations in more than 170 countries around the world. Over the past three decades, the ratio of overseas operations has expanded from 15% to over 80%, and the number of Group employees has rapidly increased from 13,000 to 100,000.</p>
<p>The driving force behind this growth is the Group&rsquo;s unique corporate and organizational cultures fostered over the course of the past century. In order to review these strengths and pass them on to the next generation, we revised Our Group Philosophy on the occasion of this 100th anniversary. Based on society&rsquo;s expectations and demands for Daikin, we have demonstrated our commitment to the sustainable development of society by working to resolve social issues with this philosophy as the basic management concept that will support further growth and development in the future.</p>
</div>

<div class="g-section-lv2">
<h2 class="g-ttl-2">Continuing to Live Up to the Expectations of Stakeholders</h2>
<p>Daikin supports the 10 principles of the United Nations Global Compact. We are also actively involved in various international initiatives, including the Task Force on Climate-related Financial Disclosures (TCFD) and the World Business Council for Sustainable Development (WBCSD).</p>
<p>We will engage in mutual communication with our stakeholders, including national and local governments, international organizations, and NGOs, and appropriately address the demands and expectations of society as a company that creates value for society and grows alongside its stakeholders.</p>
<p class="g-talign-r"><strong>Masanori Togawa</strong><br>
Chairman and CEO<br>
Daikin Industries, Ltd.</p>
</div></div>





