<div class="g-section-lv2">
<h2 id="1" class="g-ttl-2">1. Resolve Social Issues and Enhance Corporate Value</h2>
<p class="g-p g-mb-10 g-ml-30">
Daikin will contribute to the sustainable development of society through solid business activities around the world. </p>
<p class="g-p g-ml-30">With <span style="font-style: italic;">Air</span>, integral to the global environment and the core of our business, we will commit to solving environmental and social issues while enhancing corporate value.
</p>
</div>
<div class="g-section-lv2">
<h2 id="2" class="g-ttl-2">2. Create New Value by Anticipating Future Needs</h2>
<p class="g-p g-mb-10 g-ml-30">
Daikin will identify and realize the future needs of customers through deep insight into global trends and extensive marketing activities. </p>
<p class="g-p g-ml-30">We will continue to create new value that gives fresh excitement and joy to our customers and society by providing high-quality products, materials, services, and solutions as a responsible manufacturer.
</p>
</div>
<div class="g-section-lv2">
<h2 id="3" class="g-ttl-2">3. Realize a Better Society through Innovative Technologies</h2>
<p class="g-p g-mb-10 g-ml-30">
Daikin will create leading-edge technologies across all business areas and contribute to realizing a better society. </p>
<p class="g-p g-ml-30">We will create new value and businesses by refining our core technologies around air quality, energy efficiency, and environmental solutions through collaborative innovation with our partners.
</p>
</div>
<div class="g-section-lv2">
<h2 id="4" class="g-ttl-2">4. Take Action to Maintain Society's Trust</h2>
<h3 class="g-ttl-3">(1) Be Transparent to Society and Build Mutual Development</h3>
<p class="g-p g-mb-10 g-ml-30">
Daikin observes all applicable laws and regulations, acts in accordance with the highest ethical standards, and conducts business based on fair competition, transparency, and accountability. By actively engaging in dialogue with external stakeholders, we aim to ensure their correct understanding of our management policies and corporate culture. At the same time, we are eager to learn from society and continue to grow together. </p>
<p class="g-p g-ml-30">Daikin respects the diversity in the cultures and customs of the global communities where we operate. We fulfill our corporate social responsibilities by working closely and  communicating proactively with local communities, and making our own unique contributions to support their future growth and prosperity. We aim to become a respected and trusted company.
</p>
<h3 class="g-ttl-3">(2) Grow with our Business Partners</h3>
<p class="g-p g-ml-30">
Daikin strongly promotes collaborative and trustworthy growth with our associates and strategic partners in all areas of management, including procurement, production, sales, and R&amp;D. We meet each other's expectations in our respective positions, seeking to grow and develop together.
</p>
</div>
<div class="g-section-lv2">
<h2 id="5" class="g-ttl-2">5. Think Globally and Be Flexible and Vibrant</h2>
<p class="g-p g-mb-10 g-ml-30">
Daikin Group Companies will share information and transfer know-how within our organization to develop and expand our respective businesses while creating effective synergies and harmony with a global perspective. </p>
<p class="g-p g-ml-30">The relationship between Daikin Industries, Ltd. and the Group Companies will be flexible and adaptive to changing circumstances. Daikin Industries, Ltd. and Group Companies will ensure effective two-way communication and share frontline management information based on group-wide policies and rules. Group companies, which have a deeper understanding of their own businesses, will play active roles with self-initiatives, while Daikin Industries, Ltd. will support them to realize the future growth of Daikin Group globally.
</p>
</div>
<div class="g-section-lv2">
<h2 id="6" class="g-ttl-2">6. Practice &ldquo;People-Centered Management (PCM)&rdquo;
and Provide Challenging Opportunities</h2>
<p class="g-p g-mb-10 g-ml-30">Daikin truly believes that &ldquo;Each individual possesses infinite potential&rdquo; and &ldquo;The cumulative growth of all employees is the foundation for the company's development&rdquo;. We have practiced PCM since Daikin was founded by bringing out the motivation and commitment of people and combining the strengths of each individual into the power of the organization. </p>
<p class="g-p g-ml-30">With a belief in the inherent goodness of people, Daikin is fully committed to empowering them who are the source of our competitiveness and enabling them to take bold actions and seek growth.
</p>
<h3 class="g-ttl-3">(1) Create an Open-minded Atmosphere and Provide Ambitious Challengers with More Opportunities</h3>
<p class="g-p g-mb-10 g-ml-30">
Daikin encourages an environment of deep and extensive discussions and &ldquo;Flat and Fast management&rdquo; to foster the motivation and commitment of each individual where the final decision is supported by all. </p>
<p class="g-p g-ml-30">We do not criticize employees for forward-looking failures, value their caring attitude, and allow them vigorous challenges. We support those who take on challenges with high aspirations and give opportunities to those who contribute to the organization.
</p>
<h3 class="g-ttl-3">(2) Value Empathy for Daikin Group Philosophy and Cherish the Pride and Joy of Being Part of the Daikin Group</h3>
<p class="g-p g-mb-10 g-ml-30">
Daikin believes that companies and individuals are drawn together in a relationship of mutual preference. We strive to create a harmonious environment where employees are proud to be a member of Daikin Group and share the joy, resulting in them having a greater sense of fulfillment in life. </p>
<p class="g-p g-ml-30">In return, all employees are expected to act in accordance with Daikin Group Philosophy, have a sense of belonging, and work together to pursue our goals.
</p>
<h3 class="g-ttl-3">(3) Promote and Respect Diversity Management</h3>
<p class="g-p g-mb-10 g-ml-30">
Daikin respects unique individualities and the differences among people, and fosters a fair environment where all employees unleash their potential, regardless of nationality, race, gender, culture, customs, beliefs etc. </p>
<p class="g-p g-ml-30">We bring together diverse people who demonstrate their unique individualities and strengths and embrace healthy competition so that they will create new value and insights for the organization.
</p>
</div>
