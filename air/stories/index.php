<p class="stories-m-Text"> In addition, some customers of restaurants and beauty salons avoid visiting if these shops cannot maintain a comfortable temperature while taking measures to ventilate.</p>
<p class="stories-m-Text"> We have received feedback from customers who have used the system such as "Ventilation is achieved while the temperature remains comfortable," and "Thanks to visible ventilation measures, we have created an environment where our customers can use our services with peace of mind."</p>
