<h3 class="stories-m-Heading1">Heat reclaim ventilation that can ventilate while maintaining room temperature<br>
(fast development to meet the needs)</h3>
<p class="stories-m-Text"> There is growing interest in ventilation as a preventive measure against virus infection.
However, in Japan, the climate is hot and humid in the summer and dry and cold in the winter, so opening the windows and ventilating the room will not keep the room temperature comfortable.</p>
<p class="stories-m-Text"> Ventilation concerns were also felt by various shops and facilities.
Therefore, Daikin developed a retrofit ventilation product to allow spaces with existing air conditioners to have ventilation.
While ventilating, fresh air from the outside can come in while keeping the comfortable temperature.
Shops, clinics, and elderly care facilities previously concerned about how to ventilate now benefit from and appreciate this product.</p>
<p class="stories-m-Text"> Daikin achieves both comfort and peace of mind by introducing temperature control with ventilation. </p>
